object Demo {
   def main(args: Array[String]) {
      // Định nghĩa bằng VAR
      var myVar1 :Int = 10;
      // Định nghĩa bằng VAL
      val myVar2 :String = "Hello Scala with datatype declaration.";
      var myVar3 = 20;
      val myVar4 = "Hello Scala new without datatype declaration.";
      
      println(myVar1);
      println(myVar2);
      println(myVar3);
      println(myVar4); 
   }
}